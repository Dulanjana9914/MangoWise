def market_predict(request):

    data = request.json
    location = data['Location']
    variety = data['Variety']
    month = data['Month']
    steps = data['Steps']

    print("location>> ", location)
    print("variety>> ", variety)
    print("Month>> ", month)
    print("steps>> ", steps)


    

    for x in months:
        globals()[f'model_{x}'] = None

        try:
            if globals()[f'model_{x}'] is None:
                download_blob(
                    BUCKET_NAME,
                    f"models/market/{x}.pickle",
                    f"/tmp/{x}.pickle",
                )

                print(f'model_{x} Downloaded')

                with open(f"/tmp/{x}.pickle", "rb") as model_file:
                    globals()[f'model_{x}'] = pickle.load(model_file)

                print(f'model_{x} loaded')

        except Exception as e:
            print("Error loading the model", e)
            return {"model download error":str(e) }
        
    try:
        # Check if location and variety are valid
        if location not in location_criteria or variety not in variety_criteria:
            return {"error": "Invalid location or variety."}

        # Map location and variety to binary values based on criteria
        location_binary = location_criteria[location]
        variety_binary = variety_criteria[variety]

        # Combine the binary values into a single list
        exog_values = location_binary + variety_binary

        forecasted_values = []

        for step in range(steps):
            month = month + step

            if month > 12:
                month = month - 12

            print("Month: ", month)

            if month == 1:
                loaded_model = model_JAN
            elif month == 2:
                loaded_model = model_FEB
            elif month == 3:
                loaded_model = model_MAR
            elif month == 4:
                loaded_model = model_APR
            elif month == 5:
                loaded_model = model_MAY
            elif month == 6:
                loaded_model = model_JUN
            elif month == 7:
                loaded_model = model_JUL
            elif month == 8:
                loaded_model = model_AUG
            elif month == 9:
                loaded_model = model_SEP
            elif month == 10:
                loaded_model = model_OCT
            elif month == 11:
                loaded_model = model_NOV
            elif month == 12:
                loaded_model = model_DEC

        
            forecast = loaded_model.forecast(steps=1, exog=exog_values)

            print("Forecast: ", forecast)
            forecasted_value = round(float(forecast), 2)

            print("Forecasted value: ", forecasted_value)

            forecasted_values.append(forecasted_value)
        print("Forecasted values:>>>> ", forecasted_values)
        return {"forecasted_values": forecasted_values}
    
    except Exception as e:
        print("Error making the prediction", e)
        return {"prediction error": str(e)}

       

